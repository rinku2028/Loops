using System;
using System.Collections.Generic;
using System.Text;

namespace ConsoleApp5
{
    class forloop
    {
        static void Main(string[] args)
        {
            //Printing values from 0 to 9 in loop
            int x;
            for (x = 0; x<10; x++)
            {
                Console.WriteLine("Value of x:{0}", x);
            }
            Console.ReadLine();
        }
    }
}
